async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    args = context.args
    # QR deep link — skip location selection if known
    if args and args[0] in LOCATIONS:
        loc = args[0]
        context.user_data["loc"] = loc
        await update.message.reply_text(
            f"🏥 *أهلاً بك في مستشفى جامعة قناة السويس*\n\n"
            f"📍 *موقعك:* {LOCATIONS[loc]}\n\n"
            f"👇 *إيه وجهتك؟*",
            parse_mode="Markdown",
            reply_markup=destination_keyboard()
        )
    else:
        await update.message.reply_text(
            "🏥 *أهلاً بك في مستشفى جامعة قناة السويس*\n\n"
            "📍 *أنت فين دلوقتي؟*",
            parse_mode="Markdown",
            reply_markup=location_keyboard()
        )

async def handle_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data

    # ── اختيار الموقع ──
    if data.startswith("loc:"):
        loc = data.split(":")[1]
        context.user_data["loc"] = loc
        await query.edit_message_text(
            f"📍 *موقعك:* {LOCATIONS[loc]}\n\n👇 *إيه وجهتك؟*",
            parse_mode="Markdown",
            reply_markup=destination_keyboard()
        )

    # ── اختيار الوجهة ──
    elif data.startswith("dst:"):
        dst = data.split(":")[1]
        loc = context.user_data.get("loc", "LOC_MAIN_GATE")
        route = ROUTES.get(dst, {}).get(loc)
        if route:
            await query.edit_message_text(
                route,
                parse_mode="Markdown",
                reply_markup=restart_keyboard()
            )
        else:
            await query.edit_message_text(
                "⚠️ المسار ده مش متاح دلوقتي.",
                reply_markup=restart_keyboard()
            )

    # ── إعادة تشغيل ──
    elif data == "restart_dst":
        loc = context.user_data.get("loc", "LOC_MAIN_GATE")
        await query.edit_message_text(
            f"📍 *موقعك:* {LOCATIONS[loc]}\n\n👇 *إيه وجهتك؟*",
            parse_mode="Markdown",
            reply_markup=destination_keyboard()
        )
    elif data == "restart_loc":
        await query.edit_message_text(
            "📍 *أنت فين دلوقتي؟*",
            parse_mode="Markdown",
            reply_markup=location_keyboard()
        )

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Keyword fallback for free text"""
    text = update.message.text.lower()
    keywords = {
        "عيادات": "clinics",  "عيادة": "clinics",
        "جديد":   "new_bld",
        "عزل":    "isolation", "باطنة": "isolation",
        "طوارئ":  "emergency",
        "mri":    "mri_icu",  "رنين": "mri_icu",
        "icu":    "mri_icu",  "عناية": "mri_icu", "قسطرة": "mri_icu",
        "تحاليل": "lab",      "معمل": "lab",
        "اشعة":   "xray",     "أشعة": "xray",    "سونار": "xray",
        "صيدل":   "pharmacy",
        "اورام":  "oncology", "أورام": "oncology",
    }
    for kw, dst in keywords.items():
        if kw in text:
            loc = context.user_data.get("loc", "LOC_MAIN_GATE")
            route = ROUTES.get(dst, {}).get(loc)
            if route:
                await update.message.reply_text(
                    route, parse_mode="Markdown",
                    reply_markup=restart_keyboard()
                )
            return
    await update.message.reply_text(
        "مش فاهم 😅\nاضغط /start واختار من القائمة.",
    )

