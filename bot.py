from clinics_data import search_clinic, format_clinic_card, get_today_clinics, CLINICS

async def clinic_search(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """بحث عن عيادة — /clinic اسم التخصص"""
    if not context.args:
        # عرض قائمة اليوم
        import datetime
        weekday = datetime.datetime.now().weekday()
        # Python: 0=Monday ... 5=Saturday, 6=Sunday
        # نحول لمؤشرنا: 0=سبت, 1=أحد...
        day_map = {5: 0, 6: 1, 0: 2, 1: 3, 2: 4, 3: 5}
        day_index = day_map.get(weekday, -1)
        today_clinics = get_today_clinics(day_index)

        day_names = {0:"السبت",1:"الأحد",2:"الاثنين",3:"الثلاثاء",4:"الأربعاء",5:"الخميس"}
        today_name = day_names.get(day_index, "اليوم")

        if today_clinics:
            lines = [f"📅 *عيادات {today_name}:*\n"]
            for c in today_clinics:
                lines.append(f"• {c['name']} — {c['location'].split('—')[0].strip()}")
            lines.append("\n💡 *للبحث:* اكتب /clinic ثم اسم التخصص")
            lines.append("💡 *للتوجيه:* اضغط /start")
            await update.message.reply_text(
                "\n".join(lines), parse_mode="Markdown"
            )
        else:
            await update.message.reply_text(
                "🔴 *لا توجد عيادات اليوم*\n\nأيام العمل: السبت → الخميس | 8 ص → 3 م",
                parse_mode="Markdown"
            )
        return

    query = " ".join(context.args)
    results = search_clinic(query)

    if results:
        for clinic in results[:3]:  # أقصى 3 نتائج
            await update.message.reply_text(
                format_clinic_card(clinic),
                parse_mode="Markdown"
            )
    else:
        await update.message.reply_text(
            f"❓ ما لقيتش عيادة بـ *{query}*\n\n"
            "جرب كلمة تانية أو اضغط /clinic لعيادات اليوم.",
            parse_mode="Markdown"
        )


